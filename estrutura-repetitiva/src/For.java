import java.util.Scanner;

public class For {

    //SINTAXE
    //for ( inicio ; condição ; incremento ) {
    // comando 1
    // comando 2
    // } /*

    public static void main(String[] args) {

        Scanner scan = new Scanner(System.in);

        int num = scan.nextInt();
        int soma = 0;

        for ( int i = 0; i < num; i++){
            int x = scan.nextInt();
            soma += x;
        }

        System.out.println(soma);

    }

}
